package br.com.brognoli.api.model;

public class CarneIPTU {
	
	private String inscricao;
    private String inscricaoMascara;
    private String tipo;
    private String parcela;
    private String vencimento;
    private float valor;
    private float juros;
    private String linhaDigitavel;
    private String codigoBarras;
    private String nomearquivo;
    
    
	public String getInscricao() {
		return inscricao;
	}
	public void setInscricao(String inscricao) {
		this.inscricao = inscricao;
	}
	public String getInscricaoMascara() {
		return inscricaoMascara;
	}
	public void setInscricaoMascara(String inscricaoMascara) {
		this.inscricaoMascara = inscricaoMascara;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public String getParcela() {
		return parcela;
	}
	public void setParcela(String parcela) {
		this.parcela = parcela;
	}
	public String getVencimento() {
		return vencimento;
	}
	public void setVencimento(String vencimento) {
		this.vencimento = vencimento;
	}
	public float getValor() {
		return valor;
	}
	public void setValor(float valor) {
		this.valor = valor;
	}
	public float getJuros() {
		return juros;
	}
	public void setJuros(float juros) {
		this.juros = juros;
	}
	public String getLinhaDigitavel() {
		return linhaDigitavel;
	}
	public void setLinhaDigitavel(String linhaDigitavel) {
		this.linhaDigitavel = linhaDigitavel;
	}
	public String getCodigoBarras() {
		return codigoBarras;
	}
	public void setCodigoBarras(String codigoBarras) {
		this.codigoBarras = codigoBarras;
	}
	public String getNomearquivo() {
		return nomearquivo;
	}
	public void setNomearquivo(String nomearquivo) {
		this.nomearquivo = nomearquivo;
	}
	
    
    
    
    

}
